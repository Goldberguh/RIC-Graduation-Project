    public String getGpaID(int ID)
    {
        int courseID;
        float GPA;
        int studentID = ID;
        int credits;
        Connection conn = databaseQuery.getConnection();
        PreparedStatement psSched = null;
        ResultSet rs;
        PreparedStatement psGPA = null;
        ResultSet rs2;
        int creditHours = 0;
        float gradePoints = 0;
        float GPAret;
        try
        {
            
            psSched = conn.prepareStatement("SELECT `previous_course`.`Course_ID`, `previous_course`.`GPA`, `previous_course`.`Credits`\n" +
                                            "FROM `previous_course`\n" +
                                            "WHERE previous_course.Student_ID = ?;");
            psSched.setInt(1, studentID);
            rs = psSched.executeQuery(); 
            
            while(rs.next())
            {
               courseID = rs.getInt("Course_ID");
               GPA = rs.getFloat("GPA");
               credits = rs.getInt("Credits");
               
               creditHours += credits;
               gradePoints += (GPA * credits);
               
               
            }
            
            
        }
        catch(SQLException ex)
        {
            Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        CreditsLabel.setText(String.valueOf(creditHours));
        GPAret = gradePoints/creditHours;
        DecimalFormat df = new DecimalFormat("0.00");
        
        try
        {
            
            psGPA = conn.prepareStatement("UPDATE `student` SET `Sum_GPA` = ? ,`Credits` = ?\n" +
                                          "WHERE `Student_ID` = ?;");
            psGPA.setFloat(1, GPAret);
            psGPA.setInt(2, creditHours);
            psGPA.setInt(3, studentID);
            
            psGPA.executeUpdate(); 

        }
        catch(SQLException ex)
        {
            Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
      
        
        
        
        
        
        
        return df.format(GPAret);
    }
    
