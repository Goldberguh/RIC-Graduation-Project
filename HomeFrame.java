    {
        initComponents();
        GPALabel.setText(String.valueOf(getGpaID(ID)));
        uIDLabel.setText(String.valueOf(ID));
        Connection conn = databaseQuery.getConnection();
        PreparedStatement psSched = null;
        ResultSet rs;
        
        int courseID;
        int courseNum;
        String subject;
        String courseName;
        String roomNum;
        String profLName;
        String days;
        String timeStart;
        String timeEnd;
        String dateStart;
        String dateEnd;
        int credits;
        
        try
        {
            psSched = conn.prepareStatement("SELECT `course`.`Course_ID`, `course`.`Course_Num`, `course`.`Subject`, `course`.`Course_Name`, `course`.`Room_Num`,`course`.`prof_LName`,`course`.`Days`,`course`.`Time_Start`, `course`.`Time_End`, `course`.`Date_Start`, `course`.`Date_End`, `course`.`Credits` FROM `course` WHERE `course`.Course_ID in(SELECT `current_course`.`Course_ID` FROM `current_course` WHERE `current_course`.`Student_ID` = ?)");
            psSched.setInt(1, ID);
            rs = psSched.executeQuery(); 
            
            while(rs.next())
            {
               courseID = rs.getInt("Course_ID");
               courseNum = rs.getInt("Course_Num");
               subject = rs.getString("Subject");
               courseName = rs.getString("Course_Name");
               roomNum = rs.getString("Room_Num");
               profLName = rs.getString("prof_LName");
               days = rs.getString("Days");
               timeStart = rs.getString("Time_Start");
               timeEnd = rs.getString("Time_End");
               dateStart = rs.getString("Date_Start");
               dateEnd = rs.getString("Date_End");
               credits = rs.getInt("Credits");
               
               Object[] row = {courseID, courseNum, subject, courseName, roomNum, profLName, days, timeStart, timeEnd, dateStart, dateEnd, credits};
               
               DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
               
               model.addRow(row);
            }
            
            scheduleSize = jTable1.getRowCount();
        }
        catch (SQLException ex)
        {
            Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

