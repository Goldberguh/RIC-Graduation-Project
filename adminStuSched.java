    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        //Drop Class Button
        int row = jTable1.getSelectedRow();
        int column = 0;

        if(row == -1)
        {
            JOptionPane.showMessageDialog(null, "Select a class first");
        }
        else
        {
            int response = JOptionPane.showConfirmDialog(this, "Are you sure you wish to drop this class?", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (response ==JOptionPane.YES_OPTION)
            {
                int delID = (int) jTable1.getModel().getValueAt(row, column);

                int stuID = Integer.parseInt(uIDLabel.getText());
                Connection conn = databaseQuery.getConnection();
                PreparedStatement psDel = null;
                ResultSet rs;

                try
                {
                    psDel = conn.prepareStatement("DELETE FROM `current_course` WHERE `current_course`.`Student_ID` = ? AND `current_course`.`Course_ID` = ?  ");

                    psDel.setInt(1, stuID);
                    psDel.setInt(2, delID);

                    psDel.executeUpdate();

                    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                    model.removeRow(row);
                    scheduleSize --;
                }
                catch (SQLException ex)
                {
                    Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

